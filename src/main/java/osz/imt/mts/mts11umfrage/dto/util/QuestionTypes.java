public enum QuestionTypes {
  /**
   * Boolean value answer.
   */
  BOOLEAN,
  /**
   * Single answer is possible.
   */
  SINGLEANSWER,
  /**
   * A Multiple choice question. Multiple answers can be selected.
   */
  MULTIPLECHOICE,
  /**
   * An Input question answer.
   */
  INPUT,
  /**
   * Combo fields.
   */
  COMBO

}
